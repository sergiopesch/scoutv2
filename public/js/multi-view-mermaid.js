import { escapeMermaidLabel } from "./mermaid-graph.js";

const arrays = (value) => Array.isArray(value) ? value : [];
const idOf = (value) => String(value?.id ?? "");
const sortById = (left, right) => idOf(left).localeCompare(idOf(right));
const readableLabelTheme = '"themeVariables":{"textColor":"#101115","edgeLabelBackground":"#FAFAF7"}';

function hashId(value) {
  const input = String(value ?? "");
  let hash = 2166136261;
  for (let index = 0; index < input.length; index += 1) {
    hash ^= input.charCodeAt(index);
    hash = Math.imul(hash, 16777619);
  }
  return (hash >>> 0).toString(36);
}

export function renderIdForEntity(entityId, prefix = "entity") {
  return `${prefix}_${hashId(entityId)}`;
}

const accessibleHeader = (projection, grammar) => [
  grammar,
  `accTitle: ${escapeMermaidLabel(projection.title, "Scout diagram")}`,
  `accDescr: ${escapeMermaidLabel(projection.description, "Live business diagram")}`,
  "%% All executable identifiers are generated by Scout."
];

const classes = [
  "  classDef current fill:#101115,stroke:#101115,color:#FAFAF7,stroke-width:2px",
  "  classDef desired fill:#FAFAF7,stroke:#101115,color:#101115,stroke-width:2.5px",
  "  classDef hypothesis fill:#F7F7F3,stroke:#101115,color:#101115,stroke-width:2px,stroke-dasharray:7 4",
  "  classDef unknown fill:#ECECE7,stroke:#62656C,color:#292C34,stroke-width:2px,stroke-dasharray:2 4",
  "  classDef painMarker fill:#101115,stroke:#FAFAF7,color:#FAFAF7,stroke-width:2px"
];

const presentationClass = (item) => {
  if (item?.certainty === "hypothesis") return "hypothesis";
  if (["unknown", "conflicted"].includes(item?.certainty)) return "unknown";
  if (item?.scope === "desired") return "desired";
  return ["current", "desired", "hypothesis", "unknown"].includes(item?.state)
    ? item.state
    : "unknown";
};

const labelOf = (node, prefix = "") => escapeMermaidLabel(
  `${prefix}${String(node?.shortLabel ?? node?.label ?? node?.title ?? node?.name ?? "Untitled")}`,
  "Untitled"
);

const optionalLabel = (value) => {
  const text = String(value ?? "").trim();
  return text ? escapeMermaidLabel(text) : "";
};

const typeOf = (node) => String(node?.semanticType ?? node?.type ?? node?.kind ?? "unknown")
  .toLowerCase()
  .replace(/[\s-]+/g, "_");

function nodeShape(node, id, viewKind) {
  const type = typeOf(node);
  const label = labelOf(node, node?.positionStatus === "vacant" || type === "vacancy" ? "VACANT · " : "");
  if (viewKind === "process") {
    if (["start", "end", "event"].includes(type)) return `${id}(["${label}"])`;
    if (type.includes("gateway") || type === "decision") return `${id}{"${label}"}`;
    if (["document", "artifact"].includes(type)) return `${id}[/"${label}"/]`;
    if (type === "data_store") return `${id}[("${label}")]`;
    if (type === "subprocess") return `${id}[["${label}"]]`;
    return `${id}["${label}"]`;
  }
  if (viewKind === "organization") {
    if (["org_unit", "team", "department"].includes(type)) return `${id}(["${label}"])`;
    return `${id}["${label}"]`;
  }
  if (["database", "data_store"].includes(type)) return `${id}[("${label}")]`;
  if (["queue", "event_bus"].includes(type)) return `${id}[["${label}"]]`;
  if (type === "boundary") return `${id}[["${label}"]]`;
  if (["actor", "person", "external_system"].includes(type)) return `${id}(["${label}"])`;
  return `${id}["${label}"]`;
}

function linkStyle(edge, index) {
  switch (presentationClass(edge)) {
    case "desired":
      return `  linkStyle ${index} stroke:#101115,stroke-width:3.5px`;
    case "hypothesis":
      return `  linkStyle ${index} stroke:#62656C,stroke-width:2px,stroke-dasharray:7 4`;
    case "unknown":
      return `  linkStyle ${index} stroke:#8A8C91,stroke-width:2px,stroke-dasharray:2 5`;
    default:
      return `  linkStyle ${index} stroke:#101115,stroke-width:2px`;
  }
}

function edgeConnector(edge, viewKind) {
  const type = typeOf(edge);
  if (viewKind === "organization" && (
    type.includes("secondary") || ["matrix", "functional", "project"].includes(type)
  )) return "-.->";
  const presentation = presentationClass(edge);
  if (presentation === "hypothesis" || presentation === "unknown") return "-.->";
  if (presentation === "desired") return "==>";
  return "-->";
}

function edgeDisplayLabel(edge, viewKind) {
  if (viewKind === "process") {
    return edge.label || edge.condition || (edge.isDefault ? "default" : "");
  }
  if (viewKind === "organization") return edge.label || edge.relationshipType;
  return edge.label || [edge.protocol, edge.dataDescription].filter(Boolean).join(" · ");
}

function flowchartSource(projection, { layout = "dagre", spacing = "normal" } = {}) {
  const direction = projection.viewKind === "organization" ? "TB" : "LR";
  const nodeSpacing = spacing === "wide" ? 76 : 56;
  const rankSpacing = spacing === "wide" ? 104 : 82;
  const init = `%%{init: {"layout":"${layout}",${readableLabelTheme},"flowchart":{"curve":"linear","nodeSpacing":${nodeSpacing},"rankSpacing":${rankSpacing},"padding":14}}}%%`;
  const lines = [init, ...accessibleHeader(projection, `flowchart ${direction}`)];
  const nodes = arrays(projection.nodes).slice().sort(sortById);
  const ids = new Map(nodes.map((node) => [idOf(node), renderIdForEntity(idOf(node))]));
  const processGroupsById = new Map(arrays(projection.groups).map((group) => [idOf(group), group]));

  if (nodes.length === 0) {
    lines.push(`  empty["${escapeMermaidLabel(projection.emptyMessage)}"]`);
    lines.push("  class empty unknown", ...classes);
    return lines.join("\n");
  }

  const declared = new Set();
  const declareNode = (node, indent = "  ") => {
    const id = ids.get(idOf(node));
    if (!id || declared.has(id)) return;
    const lane = projection.viewKind === "process" ? processGroupsById.get(String(node.laneId ?? "")) : undefined;
    const displayNode = lane
      ? { ...node, shortLabel: `${lane.shortLabel ?? lane.label} · ${node.shortLabel ?? node.label}` }
      : node;
    lines.push(`${indent}${nodeShape(displayNode, id, projection.viewKind)}`);
    declared.add(id);
  };
  if (projection.viewKind === "architecture" && arrays(projection.groups).length > 0) {
    const groups = arrays(projection.groups).slice().sort(sortById);
    const groupIds = new Map(groups.map((group) => [idOf(group), renderIdForEntity(idOf(group), "boundary")]));
    const groupById = new Map(groups.map((group) => [idOf(group), group]));
    const children = new Map();
    for (const group of groups) {
      const parentId = String(group.parentId ?? "");
      if (parentId && groupById.has(parentId)) {
        children.set(parentId, [...(children.get(parentId) ?? []), group]);
      }
    }
    const emitGroup = (group, indent = "  ") => {
      lines.push(`${indent}subgraph ${groupIds.get(idOf(group))}["${labelOf(group)}"]`);
      for (const node of nodes.filter((item) => item.parentBoundaryNodeId === idOf(group))) {
        declareNode(node, `${indent}  `);
      }
      for (const child of (children.get(idOf(group)) ?? []).sort(sortById)) {
        emitGroup(child, `${indent}  `);
      }
      lines.push(`${indent}end`);
    };
    for (const group of groups.filter((item) => !groupById.has(String(item.parentId ?? "")))) {
      emitGroup(group);
    }
  }
  for (const node of nodes) {
    declareNode(node);
  }
  for (const node of nodes) {
    const id = ids.get(idOf(node));
    lines.push(`  class ${id} ${presentationClass(node)}`);
  }

  const edgeStyles = [];
  let edgeIndex = 0;
  for (const edge of arrays(projection.edges).slice().sort(sortById)) {
    let from = ids.get(String(edge.from));
    let to = ids.get(String(edge.to));
    if (!from || !to) continue;
    const type = typeOf(edge);
    if (projection.viewKind === "organization" && ["primary_report", "secondary_report", "membership"].includes(type)) {
      [from, to] = [to, from];
    }
    const label = optionalLabel(edgeDisplayLabel(edge, projection.viewKind));
    const connector = edgeConnector(edge, projection.viewKind);
    lines.push(`  ${from} ${connector}${label ? `|${label}|` : ""} ${to}`);
    edgeStyles.push(linkStyle(edge, edgeIndex));
    edgeIndex += 1;
  }

  lines.push(...classes, ...edgeStyles);
  return lines.join("\n");
}

function processSwimlaneSource(projection, { spacing = "normal" } = {}) {
  const lines = [
    `%%{init: {${readableLabelTheme},"swimlane":{"nodeSpacing":${spacing === "wide" ? 68 : 48},"rankSpacing":${spacing === "wide" ? 120 : 96},"lineHops":"arc","automaticLaneOrdering":false,"ignoreCrossLaneEdges":true,"optimizeRanksByCrossings":true}}}%%`,
    ...accessibleHeader(projection, "swimlane-beta LR")
  ];
  const nodes = arrays(projection.nodes).slice().sort(sortById);
  const groups = arrays(projection.groups).slice().sort(sortById);
  const ids = new Map(nodes.map((node) => [idOf(node), renderIdForEntity(idOf(node))]));
  const groupIds = new Map(groups.map((group) => [idOf(group), renderIdForEntity(idOf(group), "lane")]));
  const laneOf = (node) => String(node?.laneId ?? node?.ownerId ?? node?.groupId ?? "");
  const unassigned = nodes.filter((node) => !groupIds.has(laneOf(node)));

  for (const group of groups) {
    const groupId = groupIds.get(idOf(group));
    lines.push(`  subgraph ${groupId}["${labelOf(group)}"]`);
    for (const node of nodes.filter((item) => laneOf(item) === idOf(group))) {
      const id = ids.get(idOf(node));
      lines.push(`    ${nodeShape(node, id, "process")}`);
    }
    lines.push("  end");
  }
  if (unassigned.length > 0) {
    lines.push('  subgraph lane_unresolved["Responsibility unresolved"]');
    for (const node of unassigned) {
      lines.push(`    ${nodeShape(node, ids.get(idOf(node)), "process")}`);
    }
    lines.push("  end");
  }
  for (const node of nodes) lines.push(`  class ${ids.get(idOf(node))} ${presentationClass(node)}`);

  const edgeStyles = [];
  let edgeIndex = 0;
  for (const edge of arrays(projection.edges).slice().sort(sortById)) {
    const from = ids.get(String(edge.from));
    const to = ids.get(String(edge.to));
    if (!from || !to) continue;
    const label = optionalLabel(edge.label || edge.condition);
    lines.push(`  ${from} ${edgeConnector(edge, "process")}${label ? `|${label}|` : ""} ${to}`);
    edgeStyles.push(linkStyle(edge, edgeIndex));
    edgeIndex += 1;
  }
  lines.push(...classes, ...edgeStyles);
  return lines.join("\n");
}

function architectureTitle(value) {
  return String(value ?? "Untitled")
    .replace(/[\u0000-\u001f\u007f]/g, " ")
    .replace(/[\[\]{}()]/g, " ")
    .replace(/\s+/g, " ")
    .trim()
    .slice(0, 70) || "Untitled";
}

function architectureIcon(node) {
  const type = typeOf(node);
  if (["database", "data_store"].includes(type)) return "database";
  if (["file_store", "disk"].includes(type)) return "disk";
  if (["cloud", "external_system"].includes(type)) return "cloud";
  if (["actor", "person", "internet"].includes(type)) return "internet";
  return "server";
}

function parentFirstGroups(groups) {
  const pending = groups.slice().sort(sortById);
  const known = new Set(pending.map(idOf));
  const emitted = new Set();
  const ordered = [];
  while (pending.length > 0) {
    const readyIndex = pending.findIndex((group) => {
      const parentId = String(group.parentId ?? "");
      return !parentId || !known.has(parentId) || emitted.has(parentId);
    });
    const index = readyIndex >= 0 ? readyIndex : 0;
    const [next] = pending.splice(index, 1);
    ordered.push(next);
    emitted.add(idOf(next));
  }
  return ordered;
}

function architectureSource(projection) {
  const lines = [
    `%%{init: {${readableLabelTheme},"architecture":{"randomize":false,"seed":1,"nodeSeparation":90,"idealEdgeLengthMultiplier":2,"numIter":1200}}}%%`,
    ...accessibleHeader(projection, "architecture-beta")
  ];
  const groups = parentFirstGroups(arrays(projection.groups));
  const nodes = arrays(projection.nodes).slice().sort(sortById);
  const groupIds = new Map(groups.map((group) => [idOf(group), renderIdForEntity(idOf(group), "boundary")]));
  const ids = new Map(nodes.map((node) => [idOf(node), renderIdForEntity(idOf(node), "service")]));

  for (const group of groups) {
    const parentId = groupIds.get(String(group.parentId ?? group.boundaryId ?? ""));
    lines.push(
      `  group ${groupIds.get(idOf(group))}(cloud)[${architectureTitle(group.label ?? group.title)}]${parentId ? ` in ${parentId}` : ""}`
    );
  }
  for (const node of nodes) {
    const parentId = groupIds.get(String(node.parentBoundaryNodeId ?? node.boundaryId ?? node.groupId ?? node.parentId ?? ""));
    lines.push(
      `  service ${ids.get(idOf(node))}(${architectureIcon(node)})[${architectureTitle(node.label ?? node.title)}]${parentId ? ` in ${parentId}` : ""}`
    );
  }
  for (const edge of arrays(projection.edges).slice().sort(sortById)) {
    const from = ids.get(String(edge.from));
    const to = ids.get(String(edge.to));
    if (from && to) lines.push(`  ${from}:R --> L:${to}`);
  }
  return lines.join("\n");
}

function flatArchitectureProjection(projection) {
  const boundaryNodes = arrays(projection.groups).map((group) => ({
    ...group,
    id: idOf(group),
    semanticType: "boundary",
    label: group.shortLabel ?? group.label ?? "Boundary",
    state: group.state ?? "current",
    scope: group.scope ?? projection.scope,
    certainty: group.certainty ?? "asserted"
  }));
  const containmentEdges = [];
  for (const group of boundaryNodes) {
    if (group.parentId) {
      containmentEdges.push({
        id: `boundary-containment:${group.parentId}:${group.id}`,
        from: String(group.parentId),
        to: group.id,
        semanticType: "containment",
        state: group.state,
        scope: group.scope,
        certainty: group.certainty
      });
    }
  }
  for (const node of arrays(projection.nodes)) {
    if (!node.parentBoundaryNodeId) continue;
    containmentEdges.push({
      id: `element-containment:${node.parentBoundaryNodeId}:${node.id}`,
      from: String(node.parentBoundaryNodeId),
      to: idOf(node),
      semanticType: "containment",
      state: node.state,
      scope: node.scope,
      certainty: node.certainty
    });
  }
  return {
    ...projection,
    nodes: [...boundaryNodes, ...arrays(projection.nodes)].sort(sortById),
    edges: [...containmentEdges, ...arrays(projection.edges)].sort(sortById),
    groups: [],
    description: `${projection.description}. Boundaries are shown as explicit containment nodes.`
  };
}

function candidateWithEdgeCoverage(id, source, projection, endpointIds) {
  const renderedEndpoints = new Set(endpointIds);
  const expectedSemanticEdgeIds = [];
  const renderedSemanticEdgeIds = [];
  const omittedSemanticEdgeIds = [];
  for (const [index, edge] of arrays(projection.edges).entries()) {
    const edgeId = idOf(edge) || `edge:${index}:${String(edge?.from ?? "")}:${String(edge?.to ?? "")}`;
    expectedSemanticEdgeIds.push(edgeId);
    if (renderedEndpoints.has(String(edge?.from)) && renderedEndpoints.has(String(edge?.to))) {
      renderedSemanticEdgeIds.push(edgeId);
    } else {
      omittedSemanticEdgeIds.push(edgeId);
    }
  }
  return {
    id,
    source,
    expectedSemanticEdgeIds,
    renderedSemanticEdgeIds,
    omittedSemanticEdgeIds
  };
}

export function compileProjectionCandidates(projection) {
  const nodeIds = arrays(projection.nodes).map(idOf);
  const completeCandidate = (id, source) => candidateWithEdgeCoverage(id, source, projection, nodeIds);
  if (projection.viewKind === "process") {
    const hasOwnedLanes = arrays(projection.groups).length > 0 && arrays(projection.nodes).some(
      (node) => node?.laneId || node?.ownerId || node?.groupId
    );
    return [
      ...(hasOwnedLanes ? [
        completeCandidate("process-swimlane-v1", processSwimlaneSource(projection)),
        completeCandidate("process-swimlane-wide-v1", processSwimlaneSource(projection, { spacing: "wide" }))
      ] : []),
      completeCandidate("process-elk-v1", flowchartSource(projection, { layout: "elk" })),
      completeCandidate("process-dagre-wide-v1", flowchartSource(projection, { layout: "dagre", spacing: "wide" }))
    ];
  }
  if (projection.viewKind === "organization") {
    return [
      completeCandidate("organization-dagre-v1", flowchartSource(projection, { layout: "dagre", spacing: "wide" })),
      completeCandidate("organization-elk-v1", flowchartSource(projection, { layout: "elk", spacing: "wide" }))
    ];
  }
  const architectureEligible = arrays(projection.nodes).length > 0 &&
    arrays(projection.nodes).length <= 12 &&
    arrays(projection.groups).length <= 4 &&
    arrays(projection.edges).every((edge) => !edge.label && !edge.protocol && !edge.dataDescription);
  return [
    ...(architectureEligible ? [completeCandidate("architecture-native-v1", architectureSource(projection))] : []),
    completeCandidate("architecture-elk-v1", flowchartSource(projection, { layout: "elk", spacing: "wide" })),
    completeCandidate("architecture-dagre-v1", flowchartSource(projection, { layout: "dagre", spacing: "wide" })),
    candidateWithEdgeCoverage(
      "architecture-flat-boundaries-v1",
      flowchartSource(flatArchitectureProjection(projection), {
        layout: "dagre",
        spacing: "wide"
      }),
      projection,
      [...nodeIds, ...arrays(projection.groups).map(idOf)]
    )
  ];
}

export function compileProjection(projection) {
  return compileProjectionCandidates(projection)[0];
}
